package com.fdmgroup;

public class Pair <T , X>{

	private T t;
	private X x;
	
	public Pair(T t, X x) {
		this.t = t;
		this.x = x;
	}


	public T getT() {
		return t;
	}
	public void setT(T t) {
		this.t = t;
	}
	public X getX() {
		return x;
	}
	public void setX(X x) {
		this.x = x;
	}

}

